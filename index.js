const numeros = [1, 2, 3, 4, 5]; // Criando um array de números

const numPares = numeros.filter(numeros => numeros % 2 === 0); // Filtrando os números pares do array utilizando o método filter com callback

console.log(numPares); //Para imprimir os números pares em um console de JavaScript real.

